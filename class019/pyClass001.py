# Introduction To dict in Python

# Create a Dictionary
thisdict = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}
print(thisdict)

# Dictionary Methods
thisdict = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}
print(thisdict.keys())
print(thisdict.values())
print(thisdict.items())

# Dictionary Length
thisdict = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}
print(len(thisdict))

# Dictionary Items Data Types
thisdict = {
    "brand": "Ford",
    "electric": False,
    "year": 1964,
    "colors": ["red", "white", "blue"]
}
print(thisdict)

# Dictionary Items - type()
thisdict = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}
print(type(thisdict))

# Dictionary Items - str()
thisdict = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}
print(str(thisdict))

