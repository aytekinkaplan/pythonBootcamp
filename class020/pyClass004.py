# Tupple vs List
# A tuple is immutable whereas a list is mutable


# Example01
my_tuple = (1, 2, 3)
print(my_tuple)

my_list = [1, 2, 3]
print(my_list)

# Example02
my_tuple = (1, 2, 3, 4, 5)
print(my_tuple)

my_list = [1, 2, 3, 4, 5]
print(my_list)


# Example03
my_tuple = (1, 2, 3)
print(type(my_tuple))

my_list = [1, 2, 3]
print(type(my_list))


# Example04
my_tuple = (1, 2, 3)
print(len(my_tuple))

my_list = [1, 2, 3]
print(len(my_list))


# Example05
