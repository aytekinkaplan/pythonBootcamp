# Discovering Tuples in Python
# Python has a built-in function called tuple() that can be used to convert any iterable object
# (like a list, a set, or a dictionary) into a tuple.


# Example01
my_tuple = tuple([1, 2, 3])
print(my_tuple)


# Example02
my_tuple = tuple({1, 2, 3})
print(my_tuple)

# Example03
my_tuple = tuple({1: 'a', 2: 'b', 3: 'c'})
print(my_tuple)

# Example04
my_tuple = tuple("Hello")
print(my_tuple)


# Example05
my_tuple = tuple((1, 2, 3))
print(my_tuple)

